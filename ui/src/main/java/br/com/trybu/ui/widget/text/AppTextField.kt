@Composable
fun appTextFieldColors() = TextFieldDefaults.colors(
    unfocusedTextColor = gray_1000,
    unfocusedIndicatorColor = gray_100,
    unfocusedContainerColor = Color.White,
    focusedTextColor = gray_1000,
    focusedIndicatorColor = gray_1000,
    focusedContainerColor = focusedContainer,
    disabledContainerColor = gray_50,
    disabledIndicatorColor = gray_100,
    disabledTextColor = gray_1000,
    disabledLeadingIconColor = gray_1000
)

